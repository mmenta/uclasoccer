			<h2>About UCLA Girls Camp</h2>
			<p>The national championship staff at UCLA offers a variety of camps for players of every level on our beautiful campus.  From 5-year-old beginner to 18-year-old elite player, there is something for everyone.  During UCLA soccer camps, you will be coached by a variety of college and club coaches as well as current UCLA team members.  Come train with national champions!

			<br /><br />
			UCLA has a strong history of creating challenging and encouraging environments for a wide range of soccer players.  Whether you are a forward, midfielder, defender or goalkeeper. BE READY for a training experience like never before. Find the right camp for you below!
			</p>
            
			<h2>Which camp is right for me?</h2>            
            <ul>
                <li>
                    <strong>UCLA College ID Camp</strong><br />
                    The College ID Camp, for girls ages 14-18, offers an intense schedule of games and instruction for high-level players with extensive competitive soccer experience and an interest in playing soccer at the collegiate level. In addition to field instruction, overnight camps offer a range of soccer-related activities, such as viewing film of international games, lectures on conditioning, and a discussion of college options, all contributing to the development of a well-rounded soccer athlete.
                </li>  
                <li>
                    <strong>UCLA Junior Elite Camp</strong><br />
                    The Junior Overnight Camp is open to girls ages 9-13 and offers a more intense and competitive experience than the day camp.  This camp is ideal for experienced players looking to improve their skills and tactical knowledge while getting a taste of college soccer.
                </li>                                    
                <li>                
                    <strong>UCLA Day Camp</strong><br />
                    The Day Camps are open to girls, ages 6-13, and run 4 days/week. These camps are focused on teaching fundamental skills and introducing basic game strategies. These are outstanding camps for those just learning soccer or for those just starting to play on teams. Campers are grouped according to age and ability. Campers are supervised 100% of the time by our staff of coaches and student-athletes. There will be fun daily competitions like "Crazy Hair Day" and "Crazy Shirt Day."
                </li>                    
            </ul><br />               

			
